print(x, "is", type(x))
x = 20	
print(x, "is", type(x))
x = 20.5	
print(x, "is", type(x))	
x = 1j	
print(x, "is", type(x))
x = ["apple", "banana", "cherry"]
print(x, "is", type(x))		
x = ("apple", "banana", "cherry")	
print(x, "is", type(x))	
x = range(6)		
print(x, "is", type(x))
x = {"name" : "John", "age" : 36}	
print(x, "is", type(x))	
x = {"apple", "banana", "cherry"}
print(x, "is", type(x))		
x = frozenset({"apple", "banana", "cherry"})
print(x, "is", type(x))		
x = True		
print(x, "is", type(x))
x = b"Hello"		
print(x, "is", type(x))
x = bytearray(5)		
print(x, "is", type(x))
x = memoryview(bytes(5))
print(x, "is", type(x))		
x = None
